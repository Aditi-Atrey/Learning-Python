#For loop

programming_languages = ["Rust", "Python", "C++", "Java"]
for language in programming_languages:
    print(language)


for char in "code":
    print(char)

#Nested for loop

categories = ["Fruit", "Vegetable"]
foods = ["Apple", "Carrot", "Banana"]
for category in categories:
    for food in foods:
        print(category, food)
